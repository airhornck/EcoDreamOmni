from src.models.task_orm import TaskORM
from src.services import human_in_loop as hil
from src.services import task_hub

pytestmark = pytest.mark.asyncio(loop_scope="function")


@pytest.fixture(autouse=True)
async def clear_db(db: AsyncSession):
    await db.execute(delete(TaskORM))
    await db.commit()
    hil._review_db.clear()
    hil._task_risk_db.clear()
    task_hub._clear_stores()
    yield


async def _create_task_in_human_wait(db: AsyncSession, workflow_template_id: str = "wf_001") -> task_hub.Task:
    t = await task_hub.create_task(db, "Review Me", workflow_template_id, 1, "acc_1", "pers_1", created_by="alice")
    await task_hub.configure(db, t.id)
    await task_hub.queue(db, t.id)
    await task_hub.start(db, t.id)
    await task_hub.wait_human(db, t.id)
    return await task_hub.get_task(db, t.id)


# ─── 1. Approve Task ───

async def test_approve_task(db: AsyncSession):
    t = await _create_task_in_human_wait(db)
    result = await hil.approve_task(db, t.id, "reviewer_1", publish_mode="immediate")
    assert result["status"] == "approved_waiting_publish"
    assert result["message"].startswith("审核已通过")
    assert (await task_hub.get_task(db, t.id)).status.value == "approved_waiting_publish"


async def test_dual_approval_requires_two_approvers(db: AsyncSession):
    t = await _create_task_in_human_wait(db, workflow_template_id="wf_publish")
    # First approval
    result1 = await hil.approve_task(db, t.id, "reviewer_1")
    assert result1["status"] == "human_wait"
    assert result1["message"].startswith("Primary approval")
    assert (await task_hub.get_task(db, t.id)).status.value == "human_wait"

    # Second approval
    result2 = await hil.approve_task(db, t.id, "reviewer_2")
    assert result2["status"] == "approved_waiting_publish"
    assert (await task_hub.get_task(db, t.id)).status.value == "approved_waiting_publish"


