async def test_review_stats(db: AsyncSession):
    t1 = await _create_task_in_human_wait(db)
    t2 = await _create_task_in_human_wait(db)
    t3 = await _create_task_in_human_wait(db)
    await hil.approve_task(db, t1.id, "r1")
    await hil.reject_task(db, t2.id, "r2", "bad")
    await hil.revise_task(db, t3.id, "r3", 0, {}, "fix it")
    stats = await hil.get_review_stats(db)
    assert stats["approved"] == 1
    assert stats["rejected"] == 1
    assert stats["revised"] == 1
    assert stats["pending"] == 0  # all resolved


