# ─── 7. Integration: full review lifecycle ───

async def test_full_review_lifecycle(db: AsyncSession):
    # Create and run task to human wait
    t = await task_hub.create_task(
        db,
        name="Content Review",
        workflow_template_id="wf_publish",
        workflow_version=1,
        account_id="acc_1",
        persona_id="pers_1",
        prompt_variables={"content_preview": "Draft content here", "agent_summary": "Safe"},
        created_by="alice",
    )
    await task_hub.configure(db, t.id)
    await task_hub.queue(db, t.id)
    await task_hub.start(db, t.id)
    await task_hub.wait_human(db, t.id)

    # Check pending
    pending = await hil.get_pending_tasks(db)
    assert len(pending) == 1
    assert pending[0].task_name == "Content Review"

    # Detail view
    detail = await hil.get_review_detail(db, t.id)
    assert detail["content_preview"] == "Draft content here"

    # Dual approval
    r1 = await hil.approve_task(db, t.id, "alice")
    assert r1["status"] == "human_wait"
    r2 = await hil.approve_task(db, t.id, "bob")
    assert r2["status"] == "approved_waiting_publish"

    # History
    history = await hil.get_review_history(db, t.id)
    assert len(history) == 2
    assert history[0].is_dual_approval is True

    # Stats
    stats = await hil.get_review_stats(db)
    assert stats["approved"] == 2
    assert stats["pending"] == 0
